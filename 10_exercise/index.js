// This program simulates a simple shopping cart by adding and removing items.
// Initialize an array to store items in the shopping cart
var shoppingCart = [];
// Add items to the shopping cart
shoppingCart.push("Apple", "Banana", "Orange");
// Remove an item from the shopping cart
shoppingCart.pop();
// Print the current items in the shopping cart
console.log("Items in the shopping cart:", shoppingCart);
